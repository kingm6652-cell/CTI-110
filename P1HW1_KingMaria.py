print("- Calculating Exponents -")

 
base = int(input("Enter an integer as the base value: "))
exponent = int(input("Enter an integer as the exponent: "))
result_exponent = base ** exponent

print()
print(f"{base} raised to the power of {exponent} is {result_exponent}!!")


print("- Addition and Subtraction -")

 
start_int = int(input("Enter a starting integer: "))
add_int = int(input("Enter an integer to add: "))
subtract_int = int(input("Enter an integer to subtract: "))
result_arithmetic = start_int + add_int - subtract_int

print()
print(f"{start_int} + {add_int} - {subtract_int} is equal to {result_arithmetic}")